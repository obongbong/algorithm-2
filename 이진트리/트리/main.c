#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <conio.h>

#include "tree.h"


int main(int argc, char* argv[])
{
	printf("Hello World\n");

	Node* root = makeTree();
	printTree(root);

	//findNode 출력
	printf("\n");
	findNode(root, 100);
	printf("\n");


	// Root의 Left 노드에 삽입할 새로운 노드 생성
	Node* new_node = createNode(150);

	// Root의 Left 노드에 새로운 노드 삽입
	insertLeftNode(root, new_node);

	// Root의 Right 노드에 삽입할 새로운 노드 생성
	new_node = createNode(350);

	// Root의 Right 노드에 새로운 노드 삽입
	insertRightNode(root, new_node);

	// 변경된 Tree 출력
	printf("\n트리에 노드를 추가한 후\n");
	printTree(root);

	_getch();
	return 0;
}